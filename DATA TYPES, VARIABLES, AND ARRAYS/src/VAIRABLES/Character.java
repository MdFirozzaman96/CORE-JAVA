
package VAIRABLES;



public class Character {
    
    public static void main(String[] args) {
        
        char ch1=88,ch2=89;
        System.out.println("ch1 : "+ch1+"\nch2 : "+ch2);
        
        char Alif=1576;
        System.out.println("Alif : "+Alif);
        
        char A=2536;
        System.out.println("Alif : "+A);
        
        /*char AA='\u0986';
        System.out.println("Sho Re Aa : "+AA);*/
        
        String qts="\"This is in double quotes\"";
        System.out.println("Quote : "+qts);
        
        String singleQuote = "\'This is in single quotes.\'";
        System.out.println("Single Quote : "+singleQuote);
        
        String name ="Md.Firozzaman";
        
        // Tab : 
        System.out.println("Name\t:\t"+name);
        
        // Backspace : 
        System.out.println("Name\b:\b"+name);
        
        // Blackslash : 
        
        System.out.println("Md. Firozzaman\\CSE\\B\\130");
        
        // Form Feed : 
        System.out.println("Name\f:\f"+name);
        
    }
    
}
