
package ARITHMETIC;


public class ArithmeticBinaryOperators {
    
    public static void main(String[] args) {
        
        int a=10,b=5;
        System.out.println("a + b = "+(a+b));
        System.out.println("a - b = "+(a-b));
        System.out.println("a * b = "+a*b);
        
        int x=42;
        double y=42.25;
        System.out.println("\na / b = "+x%10);
        System.out.println("a % f = "+(y%10));
        
    }
    
}
